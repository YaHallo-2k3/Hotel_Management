// khi biến cục bộ là true thì btn ADD là add, false là update
    boolean add_or_update_LoaiSanPham = true;
    
void tableLoaiSanPham()
{
    int row = tblLoaiSanPham.getSelectedRow();
    int column = tblLoaiSanPham.getSelectedColumn();
    if(column == 3)
    {
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION)
        {
            String maLoaiSanPham = tblLoaiSanPham.getValueAt(row, 0).toString();
            BLL_LoaiSanPham.delete(maLoaiSanPham);
            clearTextLoaiSanPham();
            BLL_LoaiSanPham.fillTableLoaiSanPham(tblLoaiSanPham);
            add_or_update_LoaiSanPham = true;
            txtMaLoaiHangHoa.setEditable(true);
        } 
    }
    if(column == 2)
    {
        String maLoaiSanPham = tblLoaiSanPham.getValueAt(row, 0).toString();
        String tenLoaiSanPham = tblLoaiSanPham.getValueAt(row, 1).toString();
        txtMaLoaiHangHoa.setText(maLoaiSanPham);
        txtTenLoaiHangHoa.setText(tenLoaiSanPham);
        add_or_update_LoaiSanPham = false;
        txtMaLoaiHangHoa.setEditable(false);
    }
}

