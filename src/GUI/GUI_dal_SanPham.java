void updateSanPham()
{
    MyCombobox myCbb = (MyCombobox) cboLoaiHangHoa.getSelectedItem();
    DTO_SanPham dto_SanPham = new DTO_SanPham(txtMaHangHoa.getText(), txtTenHangHoa.getText(), myCbb.MaString(), txtDonViTinh.getText(), HELPER.HELPER_ChuyenDoi.getSoDouble(txtGiaBan.getText()));
    BLL_SanPham.update(dto_SanPham);
    clearTextSanPham();
    BLL_SanPham.fillTableSanPham(tblSanPham);
}

boolean add_or_update_SanPham = true;

void tableSanPham()
{
    int row = tblSanPham.getSelectedRow();
    int column = tblSanPham.getSelectedColumn();
    if(column == 6)
    {
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION)
        {
            String maSanPham = tblSanPham.getValueAt(row, 1).toString();
            BLL_SanPham.delete(maSanPham);
            clearTextSanPham();
            BLL_SanPham.fillTableSanPham(tblSanPham);
            add_or_update_SanPham = true;
            txtMaHangHoa.setEditable(true);
        } 
    }
    if(column == 5)
    {
        String tenLoaiSanPham = tblSanPham.getValueAt(row, 0).toString();
        BLL_LoaiSanPham.fillTableValueTenLoaiSanPhamToCombobox(cboLoaiHangHoa, tenLoaiSanPham);
        txtMaHangHoa.setText(tblSanPham.getValueAt(row, 1).toString());
        txtTenHangHoa.setText(tblSanPham.getValueAt(row, 2).toString());
        txtDonViTinh.setText(tblSanPham.getValueAt(row, 3).toString());
        txtGiaBan.setText(tblSanPham.getValueAt(row, 4).toString());
        add_or_update_SanPham = false;
        txtMaHangHoa.setEditable(false);
    }
}

